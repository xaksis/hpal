	Phase 1 is finally done and Haplette has sure come a long way. The mid 90s looking logo on the top left is as fresh in my mind now as it was 
when I first created it in 2008. Phase 1 has been a roller coaster... design, redesign, build, rebuild, add, readd, all the fun aspects of creating 
a project like this that you can imagine. But before I go off into a poignant tangent about how much I've learnt during the course of this 
project, I should get back to why I started writing today - I wanted to answer a few questions that people have been asking me as
happy palette grounds its seminal rootlets. <br><br/>
<ol>
<li> <b>Why federated login?</b> <br/> 
	Safety! I'm not a security expert therefore I leave the security business to people who know it best... Google, yahoo, 
AOL etc. When you log in to haplette, your password is not stored with me, your email provider authenticates you and when they tell me 
that you have been authenticated, you get access to protected features like rating and reviewing places. All haplette accesses is your name 
and email address that you logged in with. 
</li>
<li><b>Why can't I add  places or dishes?</b> <br/>
	I am slowly opening up those features. I learn more everyday as I work with haplette and as I make things more secure and robust, 
I'll allow more and more access to cool features. Until then please bare with me. 
</li>
</ol>
Haplette is a pure adventure for me, and I hope that it is useful for everyone. Feel free to contact me and give me feedback/criticisms etc. 
